<?php

namespace App\Support\GeoFlow;

/**
 * 各平台 AI 引用优化策略 —— 基于 NewRank 4600 万数据 + Princeton SIGKDD + 行业实测。
 *
 * 每个平台返回：AI 引用权重排名、最优长度、Q&A 密度要求、数据密度要求、
 * 专家信号要求、标题公式、结构模板、禁止事项。
 */
final class PlatformCitationStrategy
{
    /**
     * 获取指定平台的全部 AI 引用优化策略。
     */
    public static function forTemplate(string $templateKey): string
    {
        return match ($templateKey) {
            'tech_blog' => self::techBlogStrategy(),
            'zhihu' => self::zhihuStrategy(),
            'toutiao' => self::toutiaoStrategy(),
            'baijiahao' => self::baijiahaoStrategy(),
            'news_media' => self::newsMediaStrategy(),
            'b2b_business' => self::b2bStrategy(),
            'social_shopping' => self::socialStrategy(),
            'bilibili' => self::bilibiliStrategy(),
            default => self::universalStrategy(),
        };
    }

    /**
     * CSDN / 博客园 技术博客 — DeepSeek 24.6% 引用率 (#1)
     */
    private static function techBlogStrategy(): string
    {
        return <<<'RULE'
=== 📊 技术博客 AI 引用优化（基于 NewRank 4600万数据 + AioGeoLab 实测） ===

【AI引用数据】
- DeepSeek 引用率 24.6%（所有平台 #1）
- 表格/结构化数据引用概率是纯文本的 3.7 倍
- FAQ 模块引用率占全部引用的 76%

【标题公式】
- 格式：[技术关键词] + 痛点/结果 + 场景限定
- 示例："[高并发] 线程池动态调优：实测 QPS 提升 4 倍的问题排查记录"
- 长度 20-30 字，含核心关键词

【结构模板 — "三明治法则"】
1. 导入层（10%）：问题背景 + 先说结论（前 100 字给出核心答案）
2. 技术层（70%）：方案对比 → 核心实现 → 踩坑记录 → 性能数据
3. 总结层（20%）：要点回顾 + FAQ + 延伸阅读

【Q&A 要求 — 这是最高优先级】
- 文末 ≥3 组 FAQ，每组 80-150 字
- 格式：Q: 具体技术问题？ → A: 结论 + 数据 + 对比
- 这是 AI 最爱的"拿来就用"格式

【数据要求】
- 每 500 字 ≥1 个量化数据点，标注来源和年份
- ≥1 个对比表格（方案 vs 方案，性能 vs 性能）
- 实测数据 +170% 引用率 vs 纯理论描述

【专家信号】
- ≥3 处代码注释解释（CSDN 特有加权）
- ≥1 处引用官方文档/学术论文
- 作者技术背景简述

【禁止事项】
- 纯代码块无文字说明（CSDN 降权）
- 全文无对比数据
- 标题不含核心关键词
RULE;
    }

    /**
     * 知乎 — 所有中国 AI 平台的"最大公约数"（12-35%）
     */
    private static function zhihuStrategy(): string
    {
        return <<<'RULE'
=== 📊 知乎 AI 引用优化（Kimi 35% #1, 豆包 21.8% #2, DeepSeek 19.8% #2） ===

【AI引用数据】
- 知乎是唯一被全部 6 大中国 AI 平台高频引用的平台
- Kimi 引用知乎文章占全部源 35%（最高）
- Q&A 格式天然匹配 AI 输出格式

【回答结构 — "微报告"模式】
1. 共鸣开头（200字）：从真实痛点场景切入
2. 方案对比（300字）：至少 2 个方案横向比较
3. 推荐方案（400字）：详细步骤 + 数据 + 案例
4. 个人经验（200字）：真实踩坑/实战经历
5. 总结 + CTA（100字）

【Q&A 要求】
- 回答本身就是 Q&A 单元，开头 50 字直接回答核心问题
- 每个分段用 H2 标题引导，含关键词
- 结尾总结用数字量化：效率提升 X%，成本降低 Y%

【数据要求】
- 每条观点配真实数据或案例
- 配操作截图或数据对比图
- 结尾用量化结果收尾

【专家信号】
- 个人专业背景简述（不是公司名，是个人经验）
- "我们团队实测""服务过 X 家企业后发现"
- 引用行业报告/学术研究

【禁止事项】
- 开篇推销产品（AI 直接判定为广告，0 引用）
- 只写结论不解释（知乎会被折叠，AI 不会引用不可靠来源）
- "谢邀""占坑""Mark"等无意义开头
RULE;
    }

    /**
     * 头条号 — 豆包 35% 引用率 (#1)
     */
    private static function toutiaoStrategy(): string
    {
        return <<<'RULE'
=== 📊 头条号 AI 引用优化（豆包 35% #1） ===

【AI引用数据】
- 豆包对头条号内容有极强偏好（字节系生态）
- 短句 + 编号列表格式 AI 提取率最高
- "朱雀大模型"检测 AI 生成，AI 率 >70% 会被限流

【结构模板】
1. 开头甩结论（30字内，不铺垫）
2. 正文 3-5 条要点，用 ①②③ 编号
3. "说句大实话"段落（100字，口语化）
4. 互动收尾

【Q&A 要求】
- 嵌入式 Q&A（不是章节标题，而是行内的"有人问...其实...")
- ≥2 组反问句："你可能想问...""这事说白了就是..."

【数据要求】
- ≥3 个具体数字，散落在各要点中
- 数字写在前面："做了 5 年，总结出 3 条铁律"

【禁止事项】
- "随着...的发展""在当今时代"等 AI 腔开头（朱雀检测重点）
- 每段超过 3 行（碎片阅读适配）
- 标题党、虚假夸大
RULE;
    }

    /**
     * 百家号 — 文心一言 30-40% 引用率 (#1)
     */
    private static function baijiahaoStrategy(): string
    {
        return <<<'RULE'
=== 📊 百家号 AI 引用优化（文心一言 30-40% #1） ===

【AI引用数据】
- 文心一言对百度系内容有极高权重加成
- 百度搜索索引是文心引用的前提条件
- 结构化内容（列表+对比表）引用率是纯文本的 2.5 倍

【百度 SEO 前置条件 — 必须先满足，否则 AI 根本看不到】
- 标题含核心关键词，密度 2-3%
- 配图 ≥3 张，用"如图 X 所示"引导
- 严禁外链（百度降权严重）
- 原创标识必须开启

【内容要求 — "独家信息增量"】
- 必须包含你的独家数据测试、真实照片、案例分析或亲身经历
- 纯 AI 生成文本被识别为"低质内容"，永不引用
- 行业背景 → 方案对比 → 推荐清单 → 使用建议

【Q&A 要求】
- 文末 FAQ 是关键——Q&A 结构被文心"搬运"进 AI 回答
- ≥3 组问答，每组答案 80-150 字

【数据要求】
- ≥1 个"独家数据"（你自己测的/你客户的/你调研的）
- 数据标注来源和时间

【禁止事项】
- 纯 AI 生成文本（判为低质，0 引用）
- 堆砌关键词（百度降权）
- "最好""第一""国家级""唯一"等绝对化用语
- 留微信号/手机号
RULE;
    }

    /**
     * 新闻媒体版 — 搜狐号是唯一被 4/5 大 AI 高频引用的平台
     */
    private static function newsMediaStrategy(): string
    {
        return <<<'RULE'
=== 📊 新闻媒体 AI 引用优化（搜狐号：通义 28.7% #1, DeepSeek 15-20%, Kimi 20%） ===

【AI引用数据】
- 搜狐号是唯一被 4/5 大中国 AI 平台高频引用的新闻媒体
- 通义千问对搜狐内容有最强偏好（28.7%, #1）
- "结构密度"比文章长度更重要

【结构模板 — 倒金字塔】
1. 导语（150字）：5W1H，核心事件/趋势/数据
2. 展开（300字）：背景 + 分析 + 多方观点
3. 案例（200字）：第三方视角报道企业
4. 展望（100字）：行业未来方向

【结构密度要求 — 最关键】
- 每 300-500 字必须包含 1 个结构化元素（标题/列表/表格）
- 禁止长段落无间断纯文本
- 使用 H2/H3 标题层级

【数据要求】
- 每 500 字 ≥1 个具体数据，标注来源
- 有来源的数据引用率 +40%

【多平台差异化 — 防重复判定】
- 核心数据/事实保持 100% 一致
- 标题必须 100% 重写
- 开头段落至少 60% 重写
- 视角/角度按平台调整

【禁止事项】
- 纯主观无数据的内容
- 长段落无结构纯文本
- 只有形容词没有定义的模糊表述
RULE;
    }

