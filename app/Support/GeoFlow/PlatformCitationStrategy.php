    /**
     * 通用策略 — 适用于未专门适配的平台
     */
    private static function universalStrategy(): string
    {
        return <<<'RULE'
=== 📊 GEO 通用 AI 引用优化（适用所有平台） ===

【5 步 GEO 文章结构 — 通用模板】
1. 问题式标题（15-25 字，直接回答用户搜索意图）
2. 前置 FAQ（2-4 组，引言段后紧跟，每组 80-150 字）
3. 因果链正文（问题→原因→数据支撑→结论）
4. 权威引用嵌入（[来源, 年份] 格式，≥3 种不同类型来源）
5. 知识图谱延伸（2-3 条延伸阅读/相关内容链接）

【量化引用影响】
- 添加具体数据 + 来源标注：+115% 引用概率
- 添加 FAQ 模块（3-5 对）：+40% 引用概率
- 添加专家观点/背书：+41% 引用概率
- 使用因果链结构（"因为...所以..."）：+41%
- 使用表格代替纯文本：3.7 倍引用概率
- 3+ 平台一致信息：4.7 倍引用概率

【禁止事项 — AI 永不引用的 3 类内容】
1. 纯主观无数据/结论
2. 长段落无结构纯文本
3. 只有形容词没有定义或可执行步骤
RULE;
    }
