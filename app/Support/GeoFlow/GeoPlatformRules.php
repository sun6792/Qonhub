<?php

namespace App\Support\GeoFlow;

/**
 * 各平台合规规则 + 去AI味指令 + GEO增强。
 * 弹药库改写时自动注入到 system prompt 末尾。
 */
final class GeoPlatformRules
{
    /**
     * 获取指定平台的全部发布规则（合规+去AI味+GEO增强）。
     */
    public static function forTemplate(string $templateKey): string
    {
        $parts = [
            self::deAiCommon(),
            self::geoEnhanceCommon(),
        ];

        $platformSpecific = match ($templateKey) {
            'zhihu' => self::zhihu(),
            'toutiao' => self::toutiao(),
            'baijiahao' => self::baijiahao(),
            'xiaohongshu', 'social_shopping' => self::xiaohongshu(),
            'b2b_business' => self::b2bBusiness(),
            'bilibili' => self::bilibili(),
            'news_media' => self::newsMedia(),
            'tech_blog' => self::techBlog(),
            default => '',
        };

        if ($platformSpecific !== '') {
            $parts[] = $platformSpecific;
        }

        return "\n\n" . implode("\n\n", $parts);
    }

    /**
     * 通用去AI味指令——所有平台适用。
     */
    private static function deAiCommon(): string
    {
        return <<<'RULE'
=== 🛡 真人化要求（去AI味，必须严格遵守） ===
你的文章必须读起来像一个有经验的行业从业者写的，不能有AI腔。

【句长控制】
1. 短句(5-10字)和长句(20-40字)交替使用。绝不允许每句话长度均匀。
2. 连续2个短句后必须跟1个长句，制造节奏感。
3. 偶尔用3-5字的极短句制造冲击力。

【禁用AI腔词汇——以下词完全删除】
"深入探讨""综上所述""旨在""至关重要""值得注意的是""显著提升"
"不可或缺""总而言之""首先其次最后""随着XX的发展""在当今时代"

替换方案：
  "深入探讨" → "聊聊""拆解一下"
  "综上所述" → "所以""总结一下"
  "旨在" → "目的就是""想解决"
  "至关重要" → "很关键""不搞会出事"
  "显著提升" → "涨了不少""效果比较明显"
  "值得注意的是" → "提醒一下""这里有个坑"

【口语渗透】
每300字必须插入至少1句口语化表达，例如：
  "说实话""跑了几趟才搞明白""确实""别问我怎么知道的"
  "坑我已经替大家踩过了""这事说来话长"

【结构破坏】
至少2处破坏AI的模板化结构：
  - 不用"总-分-总"，改为反问开头："你可能会问，XX为什么会这样？"
  - 直接甩结论开头，不铺垫
  - 中间插入看似跑题的个人经历："想起去年有个客户..."

【细节具体化】
所有模糊描述必须加具体数字或场景：
  "效果明显" → "上线3周，数据翻了2倍"
  "很多客户" → "去年服务了17家企业"
  "效率提升" → "原来3天的工作量现在4小时搞定"

【标点自然化】
末尾句偶尔不加标点，用开放式问题或省略号结束段落

【开头限制】
禁止以"随着XX的发展""在当今时代""近年来"开头
必须从具体场景、真实数据或反问切入
RULE;
    }

    /**
     * 通用GEO增强指令。
     */
    private static function geoEnhanceCommon(): string
    {
        return <<<'RULE'
=== 🎯 GEO引用增强（让AI大模型更愿引用你） ===

1.【FAQ必须≥3组】文中必须包含3-5组 Q&A 格式问答。这是AI最爱的"拿来就用"格式。
   格式：Q：具体问题？ A：分点回答（带数据+案例+对比）

2.【数据必须精确】每个重要数据后标注来源和时间
   ❌ "据数据显示效率有提升"
   ✅ "2025年Q4回访17家客户（样本量n=17），平均效率提升41.7%。数据来源：内部客户回访统计"

3.【对比表格强化】AI最常被问"A和B哪个好"。文中必须包含至少1组对比：
   | 维度 | 方案A | 方案B | 推荐 |
   用文字描述对比即可，不需要真的画表格

4.【结论前置】每个段落的第1句话是核心结论。AI截取前40-60字作为答案

5.【结构化标记】用明确的 H2/H3 分节标题和编号列表

6.【避免低价值词】禁止"本文""小编""笔者"等降低内容权威性的词

7.【引用友好检测】完成后自查：是否满足 标题问句化 + FAQ×3 + 数据精确 + 结论前置 + 对比内容
RULE;
    }

