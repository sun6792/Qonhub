    /**
     * 获取指定平台的全部发布规则（合规+去AI味+GEO增强）。
     */
    public static function forTemplate(string $templateKey): string
    {
        $parts = [
            self::deAiCommon(),
            self::geoEnhanceCommon(),
            PlatformCitationStrategy::forTemplate($templateKey),
        ];

        $platformSpecific = match ($templateKey) {
            'zhihu' => self::zhihu(),
            'toutiao' => self::toutiao(),
            'baijiahao' => self::baijiahao(),
            'xiaohongshu', 'social_shopping' => self::xiaohongshu(),
            'b2b_business' => self::b2bBusiness(),
            'bilibili' => self::bilibili(),
            'news_media' => self::newsMedia(),
            // v2.6.0: 技术社区平台（AI引用权重最高的一批）
            'csdn' => self::csdn(),
            'cnblogs' => self::cnblogs(),
            'juejin' => self::juejin(),
            'tencent_dev' => self::tencentDev(),
            'aliyun_dev' => self::aliyunDev(),
            'jianshu' => self::jianshu(),
            // 兼容旧模板
            'tech_blog' => self::csdn(),
            default => '',
        };

        if ($platformSpecific !== '') {
            $parts[] = $platformSpecific;
        }

        return "\n\n" . implode("\n\n", $parts);
    }

    /**
     * 通用去AI味指令——所有平台适用。
     */
    private static function deAiCommon(): string
    {
        return <<<'RULE'
=== 🛡 真人化要求（去AI味，必须严格遵守） ===
你的文章必须读起来像一个有经验的行业从业者写的，不能有AI腔。

【句长控制】
1. 短句(5-10字)和长句(20-40字)交替使用。绝不允许每句话长度均匀。
2. 连续2个短句后必须跟1个长句，制造节奏感。
3. 偶尔用3-5字的极短句制造冲击力。

【禁用AI腔词汇——以下词完全删除】
"深入探讨""综上所述""旨在""至关重要""值得注意的是""显著提升"
"不可或缺""总而言之""首先其次最后""随着XX的发展""在当今时代"

替换方案：
  "深入探讨" → "聊聊""拆解一下"
  "综上所述" → "所以""总结一下"
  "旨在" → "目的就是""想解决"
  "至关重要" → "很关键""不搞会出事"
  "显著提升" → "涨了不少""效果比较明显"
  "值得注意的是" → "提醒一下""这里有个坑"

【口语渗透】
每300字必须插入至少1句口语化表达，例如：
  "说实话""跑了几趟才搞明白""确实""别问我怎么知道的"
  "坑我已经替大家踩过了""这事说来话长"

【结构破坏】
至少2处破坏AI的模板化结构：
  - 不用"总-分-总"，改为反问开头："你可能会问，XX为什么会这样？"
  - 直接甩结论开头，不铺垫
  - 中间插入看似跑题的个人经历："想起去年有个客户..."

【细节具体化】
所有模糊描述必须加具体数字或场景：
  "效果明显" → "上线3周，数据翻了2倍"
  "很多客户" → "去年服务了17家企业"
  "效率提升" → "原来3天的工作量现在4小时搞定"

【标点自然化】
末尾句偶尔不加标点，用开放式问题或省略号结束段落

【开头限制】
禁止以"随着XX的发展""在当今时代""近年来"开头
必须从具体场景、真实数据或反问切入
RULE;
    }

